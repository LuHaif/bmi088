int sysfs_gpio_export(unsigned int gpio){
    int fd, len;
    char buf[MAX_BUF];
	// /sys/class/gpio/export
    fd = open( "/sys/class/gpio/export", O_WRONLY);//打开文件
    if (fd < 0) {
        perror("gpio/export");
        return fd;
    }
    len = snprintf(buf, sizeof(buf), "%d", gpio);//从数字变换为字符串，即1 变为”1“
    write(fd, buf, len);//将需要导出的GPIO引脚编号进行写入
    close(fd);//关闭文件
 
    return 0;
}

int sysfs_gpio_set_dir(unsigned int gpio, unsigned int out_flag){
   int fd, len;
   char buf[MAX_BUF];
// /sys/class/gpio/gpioN/direction
   len = snprintf(buf, sizeof(buf), "/sys/class/gpio/gpio%d/direction", gpio);

   fd = open(buf, O_WRONLY);//打开文件
   if (fd < 0) {
       perror(buf);
       return fd;
   }

   if (out_flag)//为1，则写入“out"，即设置为输出
        write(fd, "out", 4);
    else//为0，则写入“in"，即设置为输入
        write(fd, "in", 3);
 
    close(fd);//关闭文件
    return 0;
}

int gpio_open(unsigned int gpio){
    int fd, len;
    char buf[MAX_BUF];
	// /sys/class/gpio/gpioN/value
    len = snprintf(buf, sizeof(buf), "/sys/class/gpio/gpio%d/value", gpio);
 
    fd = open(buf, O_WRONLY);//打开文件
    if (fd < 0) {
        perror(buf);
        return fd;
    }
    return fd;
}

int gpio_init(){
    sysfs_gpio_export(ACCEL_GPIO);
    sysfs_gpio_export(GYRO_GPIO);
    sysfs_gpio_set_dir(ACCEL_GPIO,OUT);
    sysfs_gpio_set_dir(GYRO_GPIO,IN);

    ACC_FD = gpio_open(215);
    GYRO_FD = gpio_open(214);
}

int sysfs_gpio_set_value(int fd, unsigned int value)
{
    if (value)//为1，则写入“1"，即设置为输出高电平
        write(fd, "1", 2);
    else//为0，则写入“0"，即设置为输出低电平
        write(fd, "0", 2);
    return 0;
}

